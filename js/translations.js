// تعيين اللغة الافتراضية إلى الإنجليزية
let currentLang = "en";

// دالة تحديث اللغة
function updateLanguage(lang) {
    currentLang = lang;
    const t = TRANSLATIONS[lang];
    if (!t) return;
    
    // تحديث النصوص في الصفحة
    const elements = [
        'tagline', 'homeIntro', 'homeFlightsTitle', 'homeElectronicsTitle',
        'homeCarsTitle', 'homeJobsTitle', 'homeServicesTitle', 'homeBlogTitle',
        'testimonialsTitle', 'flightSearchTitle', 'flightBookingTitle', 'bookingInfoTitle',
        'addPropertyTitle', 'addDeviceTitle', 'addCarTitle', 'addJobTitle', 'addGeneralTitle',
        'blogMainTitle', 'faqTitle', 'contactTitle', 'sendMessageTitle', 'sendBtn',
        'proSubscriptionTitle', 'proDesc', 'walletTitle', 'walletBalance', 'transactionsTitle', 'depositBtn',
        'chatsTitle', 'messagingTitle', 'ratingsTitle', 'addRating', 'totalAdsLabel',
        'pendingLabel', 'approvedLabel', 'rejectedLabel', 'performanceTitle',
        'pendingReviewsTitle', 'violationsTitle', 'agentLogTitle', 'blockedWordsTitle',
        'reportsTitle', 'reportsLogTitle', 'ministryTradeTitle', 'ministryTradeDesc',
        'ministryInfoTitle', 'hadasTitle', 'libraryTitle', 'comesaLink', 'shabaitLink', 'hadasLink',
        'aboutFooterTitle', 'aboutFooterText', 'quickLinksTitle', 'policiesTitle', 'agentFooterTitle',
        'privacyLink', 'securityLink', 'returnsLink', 'shippingLink', 'myAdsLabel', 'myMessagesLabel', 'myBalanceLabel'
    ];
    
    elements.forEach(id => {
        const el = document.getElementById(id);
        if (el && t[id]) el.innerText = t[id];
    });
    
    // تحديث أزرار التنقل
    const navElements = {
        navHome: t.navHome, navFlight: t.navFlight, navRealEstate: t.navRealEstate,
        navElectronics: t.navElectronics, navCars: t.navCars, navJobs: t.navJobs,
        navGeneral: t.navGeneral, navBlog: t.navBlog, navFaq: t.navFaq,
        navContact: t.navContact, navPayment: t.navPayment, navMessaging: t.navMessaging,
        navRatings: t.navRatings
    };
    
    for (const [id, text] of Object.entries(navElements)) {
        const el = document.getElementById(id);
        if (el) el.innerText = text;
    }
    
    // تحديث أزرار عامة
    const loginBtn = document.getElementById("loginModalBtn");
    if (loginBtn) loginBtn.innerHTML = t.login;
    
    const searchBtn = document.getElementById("searchFlightBtn");
    if (searchBtn) searchBtn.innerHTML = t.search;
    
    const confirmBtn = document.getElementById("confirmBookingBtn");
    if (confirmBtn) confirmBtn.innerHTML = t.confirm;
    
    const addRatingBtn = document.getElementById("addRatingBtn");
    if (addRatingBtn) addRatingBtn.innerHTML = t.addRating;
    
    // تحديث أزرار النشر
    const publishBtns = ['publishReBtn', 'publishElecBtn', 'publishCarBtn', 'publishJobBtn', 'publishGeneralBtn'];
    publishBtns.forEach(id => {
        const btn = document.getElementById(id);
        if (btn) btn.innerHTML = t.publish;
    });
    
    // تحديث اتجاه الصفحة (الإنجليزية والعربية RTL، التيجرينية RTL)
    if (lang === 'en') {
        document.body.className = 'lang-en';
        document.body.dir = 'ltr';
    } else {
        document.body.className = `lang-${lang}`;
        document.body.dir = 'rtl';
    }
    
    // تحديث أزرار اللغة النشطة
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === lang) btn.classList.add('active');
        else btn.classList.remove('active');
    });
    
    // حفظ اللغة المفضلة
    localStorage.setItem('preferredLang', lang);
    setCookie('lang', lang, 30);
    
    console.log(`✅ Language changed to: ${lang}`);
}

// تهيئة اللغة عند تحميل الصفحة
document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('preferredLang') || getCookie('lang') || 'en';
    updateLanguage(savedLang);
});
